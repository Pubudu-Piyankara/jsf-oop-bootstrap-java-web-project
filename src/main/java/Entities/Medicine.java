package Entities;

/**
 *
 * @author ASUS
 */
public class Medicine {
    private String name;
    private String code;
    private double rate;
    private String genericName;

    // Constructor
    public Medicine() {
    }

    public Medicine(String name, String code, double rate, String genericName) {
        this.name = name;
        this.code = code;
        this.rate = rate;
        this.genericName = genericName;
    }

    // Getters and Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public double getRate() {
        return rate;
    }

    public void setRate(double rate) {
        this.rate = rate;
    }

    public String getGenericName() {
        return genericName;
    }

    public void setGenericName(String genericName) {
        this.genericName = genericName;
    }

 
}
